#include <avr/io.h>


int main(void)
{
	DDRA = 0xFF;
	int num[10]={0b00111111,0b00000110,0b01011011,0b01001111,0b01100110,0b01101101,0b01111101,0b00000111,0b01111111,0b01101111};

	
	while (1)
	{
		////--------0------------------------
		if ((PINB&0b00000001)>0 )
		{
			
			PORTA=num[0];
			
			
		}
		
		//-------1--------------------------------------
		if ((PINB&0b00000010)>0 )
		{
			
			PORTA=num[1];
			
			
		}

		//----------2-----------------------
		if ((PINB&0b00000100)>0 )
		{
		
		PORTA=num[2];
		
		
		}
		//--------3--------------------------
		if ((PINB&0b00001000)>0 )
		{
		
		PORTA=num[3];
		
		
		}
		//---------4---------------------------
		if ((PINB&0b00010000)>0 )
		{
		
		PORTA=num[4];
		
		
		}
		//----------|----------------------------------------
		//----------|----------------------------------------
		//----------|----------------------------------------
		//----------V----------------------------------------
		//----------5----------------------------------------
		if ((PINC&0b00000001)>0 )
		{
		
		PORTA=num[5];
		
		
		}
		//----------6------------------------------
		if ((PINC&0b00000010)>0 )
		{
		
		PORTA=num[6];
		
		
		}
		
		//----------7------------------------------
		if ((PINC&0b00000100)>0 )
		{
		
		PORTA=num[7];
		
		
		}
		//----------8------------------------------
		if ((PINC&0b00001000)>0 )
		{
		
		PORTA=num[8];
		
		
		}
		//----------9------------------------------
		if ((PINC&0b00010000)>0 )
		{
		PORTA=num[9];
		
		
		}
		
		
		
		
	}
}
